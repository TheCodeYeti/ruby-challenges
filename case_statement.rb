weather = "apocalyptic"
case weather
when "sunny"
    puts "I love sunny days!"
when "cloudy"
    puts "At least you won't get sunburn!"
when "rainy"
    puts "Oh well, hopefully it'll be better tomorrow."
when "snowy"
    puts "Brrr! so cold!"
when "foggy"
    puts "I can't see a thing out here!"
when "windy"
    puts "Careful not to get blown over!"
else
    puts "Oh my goodness, that doesn't sound like a normal day at all!"
end
